        film4Button.setText(wypozyczalnia.znajdzNazweFilmu((numerStrony-1)*5+3));
        film5Button.setText(wypozyczalnia.znajdzNazweFilmu((numerStrony-1)*5+4));
        iloscFilmu=wypozyczalnia.znajdzIloscFilmu(film1Button.getText());
