        numerStronyText.setText("Strona: "+numerStrony);
        film1Button.setText(wypozyczalnia.znajdzNazweFilmu((numerStrony-1)*5));
        film2Button.setText(wypozyczalnia.znajdzNazweFilmu((numerStrony-1)*5+1));
